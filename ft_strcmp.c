int	ft_strcmp(char *s1, char *s2)
{
	if (s1 && s2)
	{
		if ((s1[0] || s2[0]) && s1[0] != s2[0])
			return (s1[0] - s2[0]);
		if (s1[0] && s1[0] == s2[0])
			return (ft_strcmp(s1 + 1, s2 + 1));
	}
	return (0);
}
