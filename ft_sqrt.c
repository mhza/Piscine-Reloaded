int	ft_sqrt(int nb)
{
	int i;

	if (nb > 0)
	{
		if (nb > 2)
		{
			i = 0;
			while (i++ < nb / 2 && i < 46341)
				if (i * i == nb)
					return (i);
		}
		else
			return (nb);
	}
	return (0);
}
